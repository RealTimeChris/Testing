		template<typename SimdBase256> inline static int32_t trailingZeroCount(SimdBase256 toCount) {
			int32_t returnValue{};
			for (size_t x = 0; x < 4; ++x) {
				auto value = _tzcnt_u64(toCount.getUint64(x));
				if (value < 64) {
					return value;
				}
				returnValue += value;
			}
			return returnValue;
		}

		template<typename SimdBase256> inline static uint32_t copyAndFind(const char* src, char* dst) {
			SimdBase256 values{ reinterpret_cast<const char*>(src) };

			values.store(reinterpret_cast<char*>(dst));
			for (size_t x = 0; x < 32; ++x) {
				if (src[x] == '\"') {
					dst[x] = '\0';
					return x;
				}
			}

			return 0;
		}

		inline static char* parseString(const char* src, char* dst, size_t length) {
			int32_t index{};
			while (length > 0) {
				if (auto result = copyAndFind<SimdBase256>(src + index, dst + index); result != 0) {
					return dst + result;
				}
				length -= 32;
				index += 32;
			}
			return nullptr;
		}
