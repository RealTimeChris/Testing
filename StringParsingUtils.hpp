		inline static size_t codepointToUtf8(uint32_t cp, char* c) {
			if (cp <= 0x7F) {
				c[0] = char(cp);
				return 1;
			}
			if (cp <= 0x7FF) {
				c[0] = char((cp >> 6) + 192);
				c[1] = char((cp & 63) + 128);
				return 2;
			} else if (cp <= 0xFFFF) {
				c[0] = char((cp >> 12) + 224);
				c[1] = char(((cp >> 6) & 63) + 128);
				c[2] = char((cp & 63) + 128);
				return 3;
			} else if (cp <= 0x10FFFF) {
				c[0] = char((cp >> 18) + 240);
				c[1] = char(((cp >> 12) & 63) + 128);
				c[2] = char(((cp >> 6) & 63) + 128);
				c[3] = char((cp & 63) + 128);
				return 4;
			}

			return 0;
		}

